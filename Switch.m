s =input('Enter any number from 1 to 7:');
switch s
    case 1
        disp('Sunday');
    case 2
        disp('Monday');
    case 3
        disp('Tuesday');
    case 4
        disp('Wednesday');
    case 5
        disp('Thursday');
    case 6
        disp('Friday');
    case 7
        disp('Saturday');
    otherwise
        disp('Invalid Choice');
end